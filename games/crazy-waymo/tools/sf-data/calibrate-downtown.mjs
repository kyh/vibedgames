// Calibrate the downtown model into game world space by maximising the hit
// rate of its road vertices against our baked street mask, then emit
// src/world/sf-buildings.ts (real footprints + heights, world units).
//
//   node tools/sf-data/calibrate-downtown.mjs

import { readFileSync, writeFileSync } from "node:fs";

const GRID_X = 244;
const GRID_Z = 200;
const ROAD_TILE = 13;
const WORLD_W = GRID_X * ROAD_TILE;
const WORLD_H = GRID_Z * ROAD_TILE;

// Decode the baked street mask (same hex format sf-streets.ts uses).
const streetsTs = readFileSync(new URL("../../src/world/sf-streets.ts", import.meta.url), "utf8");
const colsMatch = streetsTs.match(/cols: (\[[^\]]+\])/);
if (!colsMatch) throw new Error("could not parse sf-streets.ts cols");
const cols = JSON.parse(colsMatch[1]);
const maskAt = (gx, gz) => {
  const col = cols[gx];
  if (col === undefined || gz < 0 || gz >= GRID_Z) return false;
  const nibble = col.charCodeAt(gz >> 2);
  const val = nibble <= 57 ? nibble - 48 : nibble - 87;
  return (val & (8 >> (gz & 3))) !== 0;
};
const worldToCell = (x, z) => [
  Math.floor((x + WORLD_W / 2) / ROAD_TILE),
  Math.floor((z + WORLD_H / 2) / ROAD_TILE),
];

const raw = JSON.parse(readFileSync(new URL("./downtown-raw.json", import.meta.url), "utf8"));
// Deterministic subsample of road vertices.
const pts = raw.roadVerts.filter((_, i) => i % 7 === 0);
console.log(`calibrating with ${pts.length} road samples`);

// Model centroid for stable offset parametrisation.
let mcx = 0;
let mcz = 0;
for (const [x, z] of pts) {
  mcx += x;
  mcz += z;
}
mcx /= pts.length;
mcz /= pts.length;

// Hit rate with a concentration penalty: shrinking the cloud onto a dense
// mask region fakes high hit rates, so reward covering many distinct cells.
const score = (s, fz, ox, oz, stride = 3) => {
  let hits = 0;
  let total = 0;
  const cells = new Set();
  for (let i = 0; i < pts.length; i += stride) {
    const p = pts[i];
    const wx = (p[0] - mcx) * s + ox;
    const wz = (p[1] - mcz) * s * fz + oz;
    const [gx, gz] = worldToCell(wx, wz);
    total++;
    if (maskAt(gx, gz)) {
      hits++;
      cells.add(gx * 1024 + gz);
    }
  }
  return (hits / total) * Math.sqrt(cells.size / total);
};

let best = { s: 0, fz: 1, ox: 0, oz: 0, rate: 0 };
for (const fz of [1, -1]) {
  for (let s = 0.12; s <= 0.162; s += 0.003) {
    for (let ox = -1400; ox <= 1400; ox += 52) {
      for (let oz = -1200; oz <= 1200; oz += 52) {
        const r = score(s, fz, ox, oz, 9);
        if (r > best.rate) best = { s, fz, ox, oz, rate: r };
      }
    }
  }
}
console.log("coarse:", JSON.stringify(best));
// Refine around the coarse optimum.
for (let pass = 0; pass < 3; pass++) {
  const stepS = [0.002, 0.001, 0.0005][pass];
  const stepO = [13, 6.5, 3][pass];
  let improved = best;
  for (let s = best.s - 4 * stepS; s <= best.s + 4 * stepS; s += stepS) {
    for (let ox = best.ox - 4 * stepO; ox <= best.ox + 4 * stepO; ox += stepO) {
      for (let oz = best.oz - 4 * stepO; oz <= best.oz + 4 * stepO; oz += stepO) {
        const r = score(s, best.fz, ox, oz, 1);
        if (r > improved.rate) improved = { s, fz: best.fz, ox, oz, rate: r };
      }
    }
  }
  best = improved;
}
console.log("refined:", JSON.stringify(best));
if (best.rate < 0.2) {
  console.error("calibration too weak — aborting emit");
  process.exit(1);
}

// --- Emit sf-buildings.ts ---
const { s, fz, ox, oz } = best;
const out = [];
for (const b of raw.buildings) {
  if (b.w > 260 || b.d > 260) continue; // merged block-complex noise
  const h = b.h * s;
  if (h < 2 || h > 130) continue;
  const x = (b.x - mcx) * s + ox;
  const z = (b.z - mcz) * s * fz + oz;
  if (Math.abs(x) > WORLD_W / 2 || Math.abs(z) > WORLD_H / 2) continue;
  out.push([
    Math.round(x * 10) / 10,
    Math.round(z * 10) / 10,
    Math.round(b.w * s * 10) / 10,
    Math.round(b.d * s * 10) / 10,
    Math.round(h * 10) / 10,
  ]);
}
// Coverage = the DENSE core of the dataset (8th..92nd percentile), not the
// stray-outlier bbox — procedural buildings only yield where real data is
// actually thick on the ground.
const q = (arr, t) => arr.slice().sort((a, b) => a - b)[Math.floor(arr.length * t)];
const xs = out.map((b) => b[0]);
const zs = out.map((b) => b[1]);
const minX = q(xs, 0.08);
const maxX = q(xs, 0.92);
const minZ = q(zs, 0.08);
const maxZ = q(zs, 0.92);
out.sort((a, b) => b[4] - a[4]);
console.log(`emitting ${out.length} buildings, coverage x ${Math.round(minX)}..${Math.round(maxX)} z ${Math.round(minZ)}..${Math.round(maxZ)}`);
console.log("tallest world:", JSON.stringify(out.slice(0, 5)));

writeFileSync(
  new URL("../../src/world/sf-buildings.ts", import.meta.url),
  `// AUTO-GENERATED by tools/sf-data/calibrate-downtown.mjs — do not edit.
// Real downtown building footprints + heights (licensed model, OSM-derived),
// calibrated to world space (road-mask hit rate ${(best.rate * 100).toFixed(1)}%).
// Entries: [x, z, width, depth, height] in world units, tallest first.
export const SF_BUILDINGS: readonly (readonly [number, number, number, number, number])[] = ${JSON.stringify(out)};

// Axis-aligned coverage of the dataset — procedural lots yield to real
// buildings inside this box.
export const SF_BUILDINGS_BOUNDS = {
  minX: ${Math.round(minX - 10)},
  maxX: ${Math.round(maxX + 10)},
  minZ: ${Math.round(minZ - 10)},
  maxZ: ${Math.round(maxZ + 10)},
} as const;
`,
);
console.log("sf-buildings.ts written");
